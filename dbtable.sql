
CREATE TABLE S_AUTHORKBNTABLE (
  KBN NUMBER(3, 0) NOT NULL,
  NAME VARCHAR2(100)
);
CREATE UNIQUE INDEX PK_S_AUTHORKBNTABLE ON S_AUTHORKBNTABLE (KBN);


CREATE TABLE S_AUTHORTABLE (
  SEQ NUMBER(8, 0) NOT NULL,
  NAME VARCHAR2(100),
  KANA_NAME VARCHAR2(100)
);
CREATE UNIQUE INDEX PK_S_AUTHORTABLE ON S_AUTHORTABLE (SEQ);

CREATE TABLE S_BOOKAUTHORTABLE (
  BOOK_SEQ NUMBER(8, 0) NOT NULL,
  SEQ NUMBER(8, 0) NOT NULL,
  AUTHOR_SEQ NUMBER(8, 0),
  AUTHOR_KBN NUMBER(3, 0)
);
CREATE UNIQUE INDEX PK_S_BOOKAUTHOR ON S_BOOKAUTHORTABLE (BOOK_SEQ, SEQ);

CREATE TABLE S_BOOKTABLE (
  SEQ NUMBER(8, 0) NOT NULL,
  ID VARCHAR2(10),
  TITLE VARCHAR2(50),
  NO NUMBER(4, 0),
  ISBN VARCHAR2(20),
  PRICE NUMBER(8, 2),
  SERIESE_SEQ NUMBER(8, 0),
  PUBLISHER_SEQ NUMBER(8, 0)
);
CREATE UNIQUE INDEX PK_S_BOOKTABLE ON S_BOOKTABLE (SEQ);

CREATE TABLE S_PUBLISHERTABLE (
  SEQ NUMBER(8, 0) NOT NULL,
  NAME VARCHAR2(100)
);
CREATE UNIQUE INDEX PK_S_PUBLISHERTABLE ON S_PUBLISHERTABLE (SEQ);

CREATE TABLE S_SERIESETABLE (
  SEQ NUMBER(8, 0) NOT NULL,
  NAME VARCHAR2(100)
);
CREATE UNIQUE INDEX PK_S_SERIESETABLE ON S_SERIESETABLE (SEQ);

CREATE TABLE S_USERS (
  ID VARCHAR2(20),
  PASSWORD VARCHAR2(50),
  FIRSTNAME VARCHAR2(20),
  LASTNAME VARCHAR2(20),
  EMAIL VARCHAR2(100),
  LANGUAGE NUMBER(2, 0),
  DICTIONARY_VALUE_KEY VARCHAR2(10),
  PRIMARY_OFFICE_SEQ NUMBER(8, 0)
);
CREATE  INDEX PK_S_USERSINDEX ON S_USERS (ID);

CREATE TABLE S_USERROLE (
  ID VARCHAR2(20),
  ROLENAME VARCHAR2(30)
);
CREATE  INDEX PK_S_USERROLEINDEX ON S_USERROLE (ID, ROLENAME);

CREATE TABLE S_ROLE (
	ROLE VARCHAR2(10)
);
CREATE  INDEX PK_S_ROLEINDEX ON S_ROLE (ROLE);

CREATE TABLE S_OFFICE (
  SEQ NUMBER(8, 0) NOT NULL,
  NAME VARCHAR2(50)
);
CREATE INDEX PK_S_OFFICEINDEX ON S_OFFICE (SEQ);

CREATE TABLE S_DICTIONARY (
	DICTIONARY_KEY VARCHAR2(20),
	DEFAULT_VALUE VARCHAR2(200)
);
CREATE INDEX PK_S_DICTIONARYINDEX ON S_DICTIONARY (DICTIONARY_KEY);

CREATE TABLE S_DICTIONARY_VALUE (
	DICTIONARY_KEY VARCHAR2(20),
	VALUE_KEY VARCHAR2(10),
	VALUE VARCHAR2(200)
);
CREATE INDEX PK_S_DICTIONARY_VALUEINDEX ON S_DICTIONARY_VALUE (DICTIONARY_KEY, VALUE_KEY);

